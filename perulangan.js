//Perulangan FOR
console.log("\nPerulangan FOR =====")
for (var i = 1; i <= 10; i++) {
    console.log(i)
}

//Perulangan WHILE, DO WHILE
console.log("\nWHILE =====")

var angka = 1

while (angka <= 21) {
    console.log(angka++)
}

console.log("\nDO WHILE =====")
var angka1 = 2

do {
    angka1++
    console.log(angka1)
} while (angka1 < 10)




